package arraystwo;

public class Five {

	public static void main(String[] args) {

		int[][] a = { { 9, 5, 7 }, { 8, 3, 6 }, { 2, 1, 4 }, { 5, 3, 2 } };

		System.out.println(a[0][0]); //9
		System.out.println(a[0][1]); //5
		System.out.println(a[0][2]); //7

		System.out.println(a[1][0]); //8
		System.out.println(a[1][1]); //3
		System.out.println(a[1][2]); //6

		System.out.println(a[2][0]); //2
		System.out.println(a[2][1]); //1
		System.out.println(a[2][2]); //4

		System.out.println(a[3][0]); //5
		System.out.println(a[3][1]); //3
		System.out.println(a[3][2]); //2

	}

}
